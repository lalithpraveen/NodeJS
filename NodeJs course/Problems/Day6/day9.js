// Which of the following are examples of node modules?

// a. Express

// b. Body-parser

// c. Socket.io

// d. All of the above


// Answer :d. All of the above

// Explanation: All of the options listed (Express, Body-parser, and Socket.io) are examples of Node.js modules. These modules are commonly used in Node.js applications for various purposes:

// a. Express: Express is a web application framework for Node.js that provides a robust set of features for building web applications and APIs.

// b. Body-parser: Body-parser is a middleware for Express that parses incoming request bodies in middleware before your handlers, available under the req.body property.

// c. Socket.io: Socket.io is a JavaScript library that enables real-time, bidirectional, and event-based communication between web clients and servers. It is often used with Node.js to build real-time web applications, such as chat applications and online gaming platforms.
